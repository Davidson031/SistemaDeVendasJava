    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        
        try {
           
            int qtd_atualizada; 
            int qtd_estoque = Integer.parseInt(txtestoque.getText());
            qtd_nova = Integer.parseInt(txtqtd.getText());
            
            ProdutosDAO dao = new ProdutosDAO();
            qtd_atualizada = qtd_estoque + qtd_nova;
            
            dao.adicionarEstoque(idproduto, qtd_atualizada);
            JOptionPane.showMessageDialog(null, "Estoque atualizado");
        } catch (Exception e) {
        }
        
        
        
    }//GEN-LAST:event_jButton2ActionPerformed

