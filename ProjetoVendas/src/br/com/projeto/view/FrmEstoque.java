    public void listar(){
    
        ProdutosDAO dao = new ProdutosDAO(); //Cria um placeholder para PRODUTO
        List<Produtos> lista = dao.listarProdutos(); //Cria e recebe lista de produtos a ser exibida
        DefaultTableModel dados = (DefaultTableModel)tabelaProdutos.getModel();//pegando o model da tabela
        dados.setNumRows(0); //limpando tabela
        
        for(Produtos c: lista){
            
            dados.addRow(new Object[]{
                c.getId(),
                c.getDescricao(),
                c.getPreco(),
                c.getQtd_estoque(),       
                c.getFornecedor().getNome()
                
            });
        }
    }
    
    
